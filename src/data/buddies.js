export function getMatchedBuddies(userPrefs) {
  if (!userPrefs) return buddies;

  return buddies.map(buddy => {
    let score = 0;
    if (userPrefs.cuisines?.some(c => buddy.favCuisines.includes(c))) score += 30;
    if (userPrefs.dietMode === buddy.dietMode) score += 25;
    if (userPrefs.spiceLevel === buddy.spiceLevel) score += 15;
    if (userPrefs.budget === buddy.budget) score += 15;
    if (userPrefs.university === buddy.university) score += 10;
    if (userPrefs.mbti && userPrefs.mbti === buddy.mbti) score += 5;
    return { ...buddy, matchScore: Math.min(score, 98) };
  }).sort((a, b) => b.matchScore - a.matchScore);
}
