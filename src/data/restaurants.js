export function getRestaurantById(id) {
  return restaurants.find(r => r.id === Number(id));
}

export function getRestaurantsByScenario(scenarioId) {
  return restaurants.filter(r => r.scenarios.includes(scenarioId));
}

export function getRestaurantsByRanking(rankingType) {
  return restaurants
    .filter(r => r.rankings.includes(rankingType))
    .sort((a, b) => (a.rankPosition[rankingType] || 99) - (b.rankPosition[rankingType] || 99));
}

export function getNewRestaurants() {
  return restaurants.filter(r => r.isNew);
}

export function getWeeklyTrending() {
  return [...restaurants].sort((a, b) => b.weeklyTrend - a.weeklyTrend).slice(0, 10);
}
